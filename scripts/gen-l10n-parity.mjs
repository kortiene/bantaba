#!/usr/bin/env node
// Regenerates app/test/l10n_parity_test.dart from app/lib/src/l10n/arb/app_en.arb.
//
// The test pins the EN catalog: plain entries verbatim; parameterized entries
// as EXACT rendered output under distinct fixture args (a swapped or dropped
// placeholder changes the pinned string); ICU plurals additionally pinned at
// n=1 and n=2 so BOTH branches are exercised.
//
// Run after every ARB edit: node scripts/gen-l10n-parity.mjs
// (l10n_parity_test failing with "stale" symptoms means you forgot.)

import { readFileSync, writeFileSync } from 'node:fs';

const root = new URL('..', import.meta.url).pathname;
const arb = JSON.parse(
  readFileSync(`${root}/app/lib/src/l10n/arb/app_en.arb`, 'utf8'),
);

const keys = Object.keys(arb).filter((k) => !k.startsWith('@'));

function dartStr(s) {
  return `"${s
    .replaceAll('\\', '\\\\')
    .replaceAll('"', '\\"')
    .replaceAll('$', '\\$')
    .replaceAll('\n', '\\n')}"`;
}

// Extract `{name, plural, one{...} other{...}}` with brace matching.
function findPlural(msg) {
  const m = msg.match(/\{(\w+),\s*plural\s*,/);
  if (!m) return null;
  const start = m.index;
  let depth = 0;
  for (let i = start; i < msg.length; i++) {
    if (msg[i] === '{') depth++;
    if (msg[i] === '}') depth--;
    if (depth === 0) {
      const body = msg.slice(start + m[0].length, i);
      const branch = (name) => {
        const bm = body.match(new RegExp(`${name}\\s*\\{`));
        if (!bm) return null;
        let d = 0;
        for (let j = bm.index + bm[0].length - 1; j < body.length; j++) {
          if (body[j] === '{') d++;
          if (body[j] === '}') d--;
          if (d === 0) return body.slice(bm.index + bm[0].length, j);
        }
        return null;
      };
      return {
        varName: m[1],
        before: msg.slice(0, start),
        after: msg.slice(i + 1),
        one: branch('one'),
        other: branch('other'),
      };
    }
  }
  return null;
}

// Render an ICU message under concrete args (handles ≤1 plural block, which
// covers this catalog; the generator throws if a message exceeds that).
function render(msg, args) {
  const p = findPlural(msg);
  if (p) {
    const n = args[p.varName];
    const branch = n === 1 && p.one != null ? p.one : p.other;
    if (findPlural(branch)) throw new Error('nested plural unsupported');
    msg = p.before + branch + p.after;
    if (findPlural(msg)) throw new Error('multiple plurals unsupported');
  }
  return msg.replace(/\{(\w+)\}/g, (whole, name) =>
    name in args ? String(args[name]) : whole,
  );
}

const NUM_SAMPLES = [7, 13, 21, 34, 55];
const lines = [
  '/// EN catalog parity — GENERATED by scripts/gen-l10n-parity.mjs from',
  '/// app_en.arb (re-run it after every ARB edit). Pins every plain entry',
  '/// verbatim and every parameterized entry as EXACT output under distinct',
  '/// fixture args (a swapped/dropped placeholder changes the pin); ICU',
  '/// plurals are pinned at n=1 AND n=2 so both branches are exercised.',
  'library;',
  '',
  "import 'package:flutter_test/flutter_test.dart';",
  '',
  "import 'helpers.dart';",
  '',
  'void main() {',
  "  test('every plain en entry matches the catalog exactly', () {",
];

let plain = 0;
const paramCases = [];
for (const k of keys) {
  const msg = arb[k];
  const meta = arb['@' + k] ?? {};
  const placeholders = Object.entries(meta.placeholders ?? {});
  if (placeholders.length === 0) {
    lines.push(`    expect(en.${k}, ${dartStr(msg)});`);
    plain++;
    continue;
  }
  const isPlural = /\{\w+,\s*plural\s*,/.test(msg);
  const buildCase = (pluralN) => {
    const args = {};
    const dartArgs = [];
    let numIdx = 0;
    for (const [name, ph] of placeholders) {
      const t = (ph ?? {}).type ?? 'String';
      if (['int', 'num', 'double'].includes(t)) {
        const isPluralVar = isPlural && msg.includes(`{${name}, plural`);
        const v = isPluralVar && pluralN != null ? pluralN : NUM_SAMPLES[numIdx++ % NUM_SAMPLES.length];
        args[name] = v;
        dartArgs.push(String(v));
      } else {
        args[name] = `X${name}X`;
        dartArgs.push(`'X${name}X'`);
      }
    }
    return `    expect(en.${k}(${dartArgs.join(', ')}), ${dartStr(render(msg, args))});`;
  };
  paramCases.push(buildCase(null));
  if (isPlural) {
    paramCases.push(buildCase(1));
    paramCases.push(buildCase(2));
  }
}

lines.push('  });', '', "  test('every parameterized en entry renders exactly (both plural branches)', () {");
lines.push(...paramCases);
lines.push('  });', '}', '');

writeFileSync(`${root}/app/test/l10n_parity_test.dart`, lines.join('\n'));
console.log(`gen-l10n-parity: ${plain} plain pins, ${paramCases.length} parameterized pins`);
